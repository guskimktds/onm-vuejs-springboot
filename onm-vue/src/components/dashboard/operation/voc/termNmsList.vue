              <span>{{ switchString(item.oder_div_code) }}</span>
        </template>
        <template v-slot:item.change_ared_cd="{item}">
                <span>{{ switchString2(item.change_ared_cd) }}</span>
        </template> 
        </v-data-table>
         

    </base-material-card>
  </v-container>
</template>

<script>

export default {
    props: ['pList','resPagingInfo'],
    data() {
      return {
      last: 0,
      dialog: false,
      dialogDelete: false,
      editedIndex: -1,
      options: {},
