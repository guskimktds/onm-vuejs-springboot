<template>
    <v-container fluid>
      <v-card>
        <ktt-info-query 
        v-on:search="searchKTTInfo"
        v-bind:param="searchParam">></ktt-info-query>

        <ktt-info-list 
        v-bind:kList="kList"
        v-bind:kttPagingInfo="kttPagingInfo"
        @pagination="setToSearchParams"></ktt-info-list>
      </v-card>
    </v-container>
</template>

<script>
import KttInfoList from "./kttInfoList";
import KttInfoQuery from "./kttInfoQuery";
import dateInfo from "../../../utils/common"

import EventBus from '../../../../EventBus'
import axios from "axios";

const headers={
  'User-Agent': 'GiGA Eyes (compatible;DeviceType/iPhone;DeviceModel/SCH-M20;DeviceId/3F2A009CDE;OSType/iOS;OSVersion/5.1.1;AppVersion/3.0.0;IpAddr/14.52.161.208)',
  'Content-Type': 'application/json'
}

export default {
  components: {
    KttInfoList,
    KttInfoQuery
  },
  data() {
    return {
      title: "사용자 - KTT 정보",
      kList: [],
      reqPagingInfo:{
        page_no:1,
        view_cnt:10
      },
      kttPagingInfo:{},
      searchParam:{
        start_date: dateInfo().lastWeekDashFormat,
        end_date: dateInfo().currentDateDashFormat,
        date_yn:true,
        user_id:'',
        service_no:'',
        system_id:'',
        said:''
      }
    }
  },

methods: {
  searchKTTInfo: function (params) {
    
    var url=`${process.env.VUE_APP_BACKEND_SERVER_URL}/V110/ONM_13003/get_user_ktt_info_list`

    var reqParams=this.handleParams(params)

    console.log('요구하는 페이지 정보')
    console.log(reqParams)
    if(!reqParams.start_date&&!reqParams.user_id&&!reqParams.service_no&&!reqParams.system_id){
      this.$fire({
              title: "검색값을 입력해주세요.",
              type: "error"})
    }else{
      axios.post(url, reqParams, headers)
      .then((response) => {
        console.log(response)
        var resCode = response.data.res_code;
       
        if(resCode == 200){
          this.kList = response.data.data.ktt_info_list;
          this.kttPagingInfo = response.data.data.paging_info
    
        }else if(resCode==204){
          this.kList = [];
          this.kttPagingInfo = {};
          alert('KTT 정보 데이터가 없습니다.');
        }else if(resCode==410){
          alert("로그인 세션이 만료되었습니다.");
          EventBus.$emit('top-path-logout');
            this.$store
            .dispatch("LOGOUT")
            .then( res => { 
            console.log(res.status)}).catch(({ message }) => (this.msg = message))
            this.$router.replace('/signin')
        }else{
          this.kList = [];
          this.kttPagingInfo = {};
          alert("Error");
        }
      })
      .catch((ex) => {
        console.log('조회 실패',ex)
      })
      .finally(function(){})
    }
    },
    
    setToSearchParams: function(values){
      console.log(values)

      var params = {
        page_no: values.page,
        view_cnt: values.itemsPerPage
      }

      console.log(params)

      this.searchKTTInfo(params)
    },

    handleParams:function(params){
      console.log('전달된 파라미터 값')
      console.log(params)
      let newParams = {}
      if(params.date_yn==undefined){
        params.date_yn=this.searchParam.date_yn
      }

      if(params.page_no === undefined || params.page_no === ''){
        newParams.page_no = this.reqPagingInfo.page_no
      }else{
        newParams.page_no = params.page_no
      }
      if(params.view_cnt === undefined || params.view_cnt === ''){
        newParams.view_cnt = this.reqPagingInfo.view_cnt
      }else{
        newParams.view_cnt = params.view_cnt
      } 

      if(params.date_yn==true){
        if(params.start_date !== undefined && params.start_date !== ''){
          newParams.start_date = params.start_date.replace(/-/g,"")
        }else if(
          this.searchParam.start_date!==undefined&&
          this.searchParam.start_date!==""
        ){
          newParams.start_date=this.searchParam.start_date.replace(/-/g,"")
        }

        if(params.end_date !== undefined && params.end_date !== ''){
          newParams.end_date = params.end_date.replace(/-/g,"")
        }else if(
          this.searchParam.end_date!==undefined&&
          this.searchParam.end_date!==""
        ){
          newParams.end_date=this.searchParam.end_date.replace(/-/g,"")
        }
      }
      
      if(params.user_id !== undefined && params.user_id !== ''){
        newParams.user_id = params.user_id
      }else if(
        this.searchParam.user_id!==undefined&&
        this.searchParam.user_id!==""
      ){
        newParams.user_id=this.searchParam.user_id
      }
      if(params.service_no !== undefined && params.service_no !== ''){
        newParams.service_no = params.service_no
      }else if(
        this.searchParam.service_no!==undefined&&
        this.searchParam.service_no!==""
      ){
        newParams.service_no=this.searchParam.service_no
      }
      if(params.system_id !== undefined && params.system_id !== ''){
        newParams.system_id = params.system_id
      }else if(
        this.searchParam.system_id!==undefined&&
        this.searchParam.system_id!==""
      ){
        newParams.system_id=this.searchParam.system_id
      }
      if (params.said !== undefined && params.said !== "") {
        newParams.said = params.said;
      } else if (
        this.searchParam.said !== undefined &&
        this.searchParam.said !== ""
      ) {
        newParams.said = this.searchParam.said;
      }

      if(Number(newParams.start_date)-Number(newParams.end_date)>0){
        alert('형식에 맞는 날짜 검색값을 입력해주세요')
        newParams.start_date=dateInfo().lastWeekDashFormat.replace(/-/g,"")
        newParams.end_date=dateInfo().currentDateDashFormat.replace(/-/g,"")
        this.searchParam.start_date=dateInfo().lastWeekDashFormat
        this.searchParam.end_date=dateInfo().currentDateDashFormat
      }
      
      return newParams
    }
  },
}
</script>

