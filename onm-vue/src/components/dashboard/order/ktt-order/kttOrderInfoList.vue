<template>
  <v-container
      id="regular-tables"
      fluid
      tag="section"
    >

    <base-material-card
        color="orange"
        dark
        icon="mdi-keyboard"
        title="KTT 청약 정보 LIST"
        class="px-5 py-3"
      >

      <v-data-table
        :headers="headers"
        :items="kttList"
        class="elevation-1"
        :footer-props="{itemsPerPageOptions:[5,10,15,20]}"
        :header-props="{ sortIcon: null }"
      >
      </v-data-table>

    </base-material-card>
  </v-container>
</template>


<script>
export default {
  props: ["kttList", "kttPagingInfo"],
  data() {
    return {
      headers: [
        { text: '거래고유번호', value: 'guid' },
        { text: '계약 ID', value: 'said' },
        { text: '서비스번호', value: 'service_no' },
        { text: '시스템ID', value: 'system_id' },
        { text: 'KTT 계약 ID', value: 'contract_id' },
      ],
        last:0,
        options: {},
        totalList: 0,
        loading: true,

    }
  },
  methods: {
    getDataFromApi() {
      this.loading = true;
      this.$emit("pagination", this.options);
    },
  },
  watch: {
    options: {
      handler() {
        this.getDataFromApi();
      },
      deep: true,
    },
  },
  updated() {
      if(this.last!==this.kttPagingInfo.total_cnt){
        this.options.page=1
      }
      if(this.kttPagingInfo.total_cnt!==undefined){
      this.last=this.kttPagingInfo.total_cnt
      }
  },
    
}
</script>

